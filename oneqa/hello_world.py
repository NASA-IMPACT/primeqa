

def hello_world() -> str:
    """
    Return Hello, world!

    :return: "Hello, world!"
    """
    return "Hello, World!"


def hello_name(name: str) -> str:
    """
    Return Hello, {name}!

    :param name: who to say hello to
    :return: "Hello, {name}!"
    """
    return f"Hello, {name}!"
